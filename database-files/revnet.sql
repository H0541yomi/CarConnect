CREATE TABLE IF NOT EXISTS Cars
(
   CarID         INT PRIMARY KEY AUTO_INCREMENT,
   OwnerID       INT,
   Make          VARCHAR(50),
   Model         VARCHAR(50),
   ModelYear     YEAR,
   ExteriorColor VARCHAR(50),
   InteriorColor VARCHAR(50),
   PurchaseDate  DATE,
   FOREIGN KEY (OwnerID) REFERENCES Users (UserID)
       ON UPDATE CASCADE
       ON DELETE SET NULL
);


CREATE TABLE IF NOT EXISTS Car_Build
(
   BuildID   INT PRIMARY KEY AUTO_INCREMENT,
   CarID     INT,
   Exhaust   VARCHAR(128),
   Turbo     VARCHAR(128),
   Engine    VARCHAR(128),
   Wheels    VARCHAR(128),
   Downpipes VARCHAR(128),
   FOREIGN KEY (CarID) REFERENCES Cars (CarID)
       ON UPDATE CASCADE
       ON DELETE SET NULL
);


CREATE TABLE IF NOT EXISTS Car_Meta
(
   CarMetaID INT PRIMARY KEY AUTO_INCREMENT,
   CarID     INT UNIQUE NOT NULL,
   Weight    INT,
   Length    INT,
   Width     INT,
   Height    INT,
   TopSpeed  DECIMAL(6, 2),
   FuelType  VARCHAR(20),
   FOREIGN KEY (CarID) REFERENCES Cars (CarID)
       ON UPDATE CASCADE
       ON DELETE CASCADE
);


